a = 34
b = "34"
d = 223

print(a)
print(type(a))  # Output: <class 'int'>
print(b)
print(type(b))  # Output: <class 'str'>

# convert b to an integer
c = int(b)
print(c)
print(type(c))  # Output: <class 'int'>

# convert d to a string
e = str(d)
print(e)
print(type(e))  # Output: <class 'str'>
