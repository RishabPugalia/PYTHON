# This script demonstrates different data types in Python.
#integer variable
age = 34
print(age)
print(type(age))  # Output: <class 'int'>

#string variable
name = "Alice"
print(name)
print(type(name))  # Output: <class 'str'>

#float variable
cgpa = 3.9
print(cgpa)
print(type(cgpa))  # Output: <class 'float'>

#boolean variable
is_student = True # can be True or False
print(is_student)
print(type(is_student))  # Output: <class 'bool'>
